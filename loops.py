#for loop
#Used to iterate over a sequence (like a list, string, or range) and execute a block of code for each item in that sequence.

#print numbers 0 to 5
for i in range(6):
    print(i)


#While Loop: Repeats as long as a condition is True.
count = 0
while count < 5:  # Loops until count is less than 5
    print(count)
    count += 1


cnt = 0
while cnt < 10:
    print(cnt)
    cnt +=1

#print each character of word python
