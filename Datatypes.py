#Datatype is a type of data we store in the variable

#1 int
age=20
print(age)
print(type(age))

#float
num=20.00
print(num)
print(type(num))

#string
name="sneha"
print(name)
print(type(name))

#boolean
student=True
print(student)
print(type(student))

#complex
com=2+4j
print(com)
print(type(com))

#list
list=["apple","mango","chikoo"]
print(list)
print(type(list))

#tuple
tuple=("apple","mango","chikoo")
