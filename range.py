#range function in python
#The range() function is used to generate a sequence of numbers. It's commonly used in loops like for loops. You can specify the start, stop, and step values.

r = range(5)
print(r)  # Output: range(0, 5)

r = range(5)  # 0 to 4
print(list(r))

for i in range(5):
    print(i)
